package data.model;

import java.util.Date;

/**
 *
 * @author H2PhySicS
 */
public class Comment {
    private String id;
    private String uid;
    private String postId;
    private String content;
    private Date time;

    public Comment(String id, String uid, String postId, String content, Date time) {
        this.id = id;
        this.uid = uid;
        this.postId = postId;
        this.content = content;
        this.time = time;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getPostId() {
        return postId;
    }

    public void setPostId(String postId) {
        this.postId = postId;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Date getTime() {
        return time;
    }

    public void setTime(Date time) {
        this.time = time;
    }
    
    
}
